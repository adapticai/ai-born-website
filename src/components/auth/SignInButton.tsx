/**
 * Email Sign In Button
 * Pre-configured for magic link - opens modal instead of direct sign-in
 */
export function EmailSignInButton(
  props: Omit<SignInButtonProps, "provider">
) {
  const [modalOpen, setModalOpen] = useState(false);
  const searchParams = useSearchParams();
  const pathname = usePathname();

  // Get callback URL from props or search params
  const callbackUrl =
    props.callbackUrl || searchParams.get("callbackUrl") || "/";

  const handleClick = () => {
    // Track button click before opening modal
    trackAuthButtonClick('sign_in', 'email', pathname || '/');
    setModalOpen(true);
  };

  return (
    <>
      <Button
        onClick={handleClick}
        variant={props.variant || "default"}
        size={props.size || "default"}
        className={props.className}
      >
        <div className="flex items-center gap-2">
          <EmailIcon />
          <span>{props.children || "Sign in with Email"}</span>
        </div>
      </Button>

      <EmailSignInModal
        open={modalOpen}
        onOpenChange={setModalOpen}
        callbackUrl={callbackUrl}
      />
    </>
  );
}

